package com.foodie.form;

/**
 * The Class FoodRecipeForm.
 *
 * @author harleenchadha
 */
public class FoodRecipeForm {

    /** The food name. */
    private String foodName;

    /** The food cuisine. */
    private String foodCuisine;

    /** The food description. */
    private String foodDescription;

    /** The food best place. */
    private String foodBestPlace;

    /**
     * Gets the food name.
     *
     * @return the food name
     */
    public String getFoodName() {
        return foodName;
    }

    /**
     * Sets the food name.
     *
     * @param foodName
     *        the new food name
     */
    public void setFoodName(String foodName) {
        this.foodName = foodName;
    }

    /**
     * Gets the food cuisine.
     *
     * @return the food cuisine
     */
    public String getFoodCuisine() {
        return foodCuisine;
    }

    /**
     * Sets the food cuisine.
     *
     * @param foodCuisine
     *        the new food cuisine
     */
    public void setFoodCuisine(String foodCuisine) {
        this.foodCuisine = foodCuisine;
    }

    /**
     * Gets the food description.
     *
     * @return the food description
     */
    public String getFoodDescription() {
        return foodDescription;
    }

    /**
     * Sets the food description.
     *
     * @param foodDescription
     *        the new food description
     */
    public void setFoodDescription(String foodDescription) {
        this.foodDescription = foodDescription;
    }

    /**
     * Gets the food best place.
     *
     * @return the food best place
     */
    public String getFoodBestPlace() {
        return foodBestPlace;
    }

    /**
     * Sets the food best place.
     *
     * @param foodBestPlace
     *        the new food best place
     */
    public void setFoodBestPlace(String foodBestPlace) {
        this.foodBestPlace = foodBestPlace;
    }
}
